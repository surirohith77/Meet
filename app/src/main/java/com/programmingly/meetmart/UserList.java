package com.programmingly.meetmart;

public class UserList {

    String post_id;
    String post_text;
    String post_category;
    String post_added_by;
    String post_status;
    String post_created;
    String post_updated;

    public String getPost_id() {
        return post_id;
    }

    public void setPost_id(String post_id) {
        this.post_id = post_id;
    }

    public String getPost_text() {
        return post_text;
    }

    public void setPost_text(String post_text) {
        this.post_text = post_text;
    }

    public String getPost_category() {
        return post_category;
    }

    public void setPost_category(String post_category) {
        this.post_category = post_category;
    }

    public String getPost_added_by() {
        return post_added_by;
    }

    public void setPost_added_by(String post_added_by) {
        this.post_added_by = post_added_by;
    }

    public String getPost_status() {
        return post_status;
    }

    public void setPost_status(String post_status) {
        this.post_status = post_status;
    }

    public String getPost_created() {
        return post_created;
    }

    public void setPost_created(String post_created) {
        this.post_created = post_created;
    }

    public String getPost_updated() {
        return post_updated;
    }

    public void setPost_updated(String post_updated) {
        this.post_updated = post_updated;
    }
}
